class EncuestaEstudiante:
    def __init__(self, id_encuesta, semestre, frecuencia_compra, horario_preferido, horas_libres, caseta_preferida,
                 factores_influencia, tiempo_dispuesto_esperar, presupuesto, experiencia_calificacion, 
                 experiencia_fila, tiempo_espera_promedio):
        self.id_encuesta = id_encuesta
        self.semestre = semestre
        self.frecuencia_compra = frecuencia_compra
        self.horario_preferido = horario_preferido
        self.horas_libres = horas_libres
        self.caseta_preferida = caseta_preferida
        self.factores_influencia = factores_influencia
        self.tiempo_dispuesto_esperar = tiempo_dispuesto_esperar
        self.presupuesto = presupuesto
        self.experiencia_calificacion = experiencia_calificacion
        self.experiencia_fila = experiencia_fila
        self.tiempo_espera_promedio = tiempo_espera_promedio
